# Процесс для получения погоды из OpenWeatherMap API
# Функция должна:
# 1. Сделать GET запрос к https://api.openweathermap.org/data/2.5/weather?lat=55.75&lon=37.61&appid={API_KEY} (вставить API_KEY из settings)
# 2. Обработать JSON ответ (пример структуры в weather_task.md)
# 3. Создать запись в модели Weather с данными из ответа
# 4. Обработать ошибки: 401 Unauthorized (неверный ключ), 404 (город не найден), отсутствие интернета — логировать и не сохранять
# 5. Логировать процесс (успех/ошибку) в лог файл (использовать logging из Django)
#
# Использовать библиотеку requests для HTTP запросов
# Функция должна быть импортируемой и вызываемой из админки

